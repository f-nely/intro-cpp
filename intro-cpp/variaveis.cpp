#include <iostream>

using namespace std;

int main()
{
	int num = 5;
	cout << "número inteiro: " << num << endl;
	
	float real = 7.8;
	cout << "número ponto flutuante: " << real << endl;
	
	double real2 = 5.2e99;
	cout << "tipo double: " << real2 << endl;
	
	bool booleano = false;
	cout << "tipo bool: " << booleano << endl;
	
	char letra = 'b';
	cout << "tipo char: " << letra << endl;
	
	string palavra = "bola";
	cout << "tipo string: " << palavra << endl;
	
	return 0;
}
